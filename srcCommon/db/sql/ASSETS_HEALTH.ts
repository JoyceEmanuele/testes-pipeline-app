    ASSET_ID: number
    ASSET_HEALTH_HIST_ID: number
    DAC_DEVICE_ID: number
    DUT_DUO_ID:number
    DUT_DEVICE_ID: number
    DEV_ID: string
    H_DAT_REPORT: number
    H_TYPECHANGE: string
    ASSET_APPLICATION: string
    MACHINE_APPLICATION: string
